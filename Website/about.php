					<h2>Geschiedenis van onze school</h2>
					<p>In 1978 werd de Eindhovense Chinese School opgericht door de Vereniging van de Eindhovense Chinese Protestanten te Eindhoven. Toen waren er maar 20 leerlingen die elke zaterdag uit Hongkongse leerboeken Kantonees leerden in een cafézaaltje op de Grote Berg.</p>
